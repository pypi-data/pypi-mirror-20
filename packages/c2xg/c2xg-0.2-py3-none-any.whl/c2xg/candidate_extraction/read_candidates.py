#OUTPUT: List of candidates ------------------------------------------------------------------#
#---------------------------------------------------------------------------------------------#
def read_candidates(file):
    
	import pickle
	
	candidate_list = []
	
	with open(file,'rb') as f:
		candidate_list = pickle.load(f)
		
	return candidate_list
